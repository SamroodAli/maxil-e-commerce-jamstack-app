// ---------------EMAIL-------------------------
// /^\w+([.-]?\w+)*@\w+([.-]?\w+)*(\.\w{2,3})+$/
// ---------------------------------------------

// ----------------------PHONE-------------------------
// /^\(?([0-9]{3})\)?[-. ]?([0-9]{3})[-. ]?([0-9]{4})$/
// ----------------------------------------------------

// -----------------------------PASSWORD---------------------------
// /^(?=.*?[A-Z])(?=.*?[a-z])(?=.*?[0-9])(?=.*?[#?!@$%^&*-]).{8,}$/
// ----------------------------------------------------------------

// -------------------------------------ADDRESS-------------------------------------
// /^(\d+) ?([A-Za-z](?= ))? (.*?) ([^ ]+?) ?((?<= )APT)? ?((?<= )\d*)?$/
// ---------------------------------------------------------------------------------

// ------------------------ZIP-----------------------------
// /^\d{5}(-\d{4})?$/
// --------------------------------------------------------

const Validate = values => {
  // input values ={field:value}
  // return {field:valid}, eg {email:true}
  const validators = {
    email: value => /^\w+([.-]?\w+)*@\w+([.-]?\w+)*(\.\w{2,3})+$/.test(value),
    phone: value =>
      /^\(?([0-9]{3})\)?[-. ]?([0-9]{3})[-. ]?([0-9]{4})$/.test(value),
    name: value => value.length > 3,
    message: value => value.length > 3,
  }

  return Object.keys(values).reduce((valid, field) => {
    valid[field] = validators[field](values[field])
    return valid
  }, {})
}

export default Validate
